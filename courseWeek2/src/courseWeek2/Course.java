package courseWeek2;

public class Course {
	
	int id;
	String instructor;
	String name;
	int percentage;
	
	public Course() {
		
	}
	
	public Course(int id, String instructor, String name, int percentage) {
		this.id = id;
		this.instructor = instructor;
		this.name = name;
		this.percentage = percentage;
		
	}
	
}
